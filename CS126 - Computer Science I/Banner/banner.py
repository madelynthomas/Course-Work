#!/usr/bin/env python

"""

A basic ASCII banner generator.

"""


letters = {
    'a': ['####', '#  #', '####', '#  #', '#  #'],
    'b': ['####', '#  #', '####', '#  #', '####'],
    'c': ['####', '#   ', '#   ', '#   ', '####'],
    'd': ['### ', '#  #', '#  #', '#  #', '### '],
    'e': ['####', '#   ', '####', '#   ', '####'],
    'f': ['####', '#   ', '####', '#   ', '#   '],
    'g': ['####', '#   ', '# ##', '#  #', '####'],
    'h': ['#  #', '#  #', '####', '#  #', '#  #'],
    'i': ['#####', '  #  ', '  #  ', '  #  ', '#####'],
    'j': ['####', '  # ', '  # ', '  # ', '####'],
    'k': ['#  #', '# # ', '##  ', '# # ', '#  #'],
    'l': ['#   ', '#   ', '#   ', '#   ', '####'],
    'm': ['#   #', '## ##', '# # #', '#   #', '#   #'],
    'n': ['#  #', '## #', '## #', '# ##', '# ##'],
    'o': [' ## ', '#  #', '#  #', '#  #', ' ## '],
    'p': ['####', '#  #', '####', '#   ', '#   '],
    'q': [' ## ', '#  #', '#  #', '# ##', ' ###'],
    'r': [' ## ', '#  #', '### ', '# # ', '#  #'],
    's': [' ###', '#   ', '### ', '   #', '### '],
    't': ['#####', '  #  ', '  #  ', '  #  ', '  #  '],
    'u': ['#  #', '#  #', '#  #', '#  #', '####'],
    'v': ['#  #', '#  #', '#  #', ' ## ', ' ## '],
    'w': ['#   #', '#   #', '# # #', '## ##', '#   #'],
    'x': ['#  #', '#  #', ' ## ', '#  #', '#  #'],
    'y': ['#  #', '#  #', ' ## ', '  # ', ' #  '],
    'z': ['####', '   #', '  # ', ' #  ', '####']}


def print_banner(text, vertical):
    s = ('')
    if vertical:
        for i in text:
            for j in range(0, 5):
                print(letters[i][j])
            print('')
    else:
        for j in range(0, 5):
            s = ('')
            for i in text:
                s += letters[i][j]
                s += ('  ')
            print(s)


print_banner('abcdefghijklmnopqrstuvwxyz', False)
