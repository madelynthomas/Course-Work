function output = say_ones(num) % display the number inputed in ones place
    if num == 1
        output = 'one';
    elseif num == 2
        output = 'two';
    elseif num == 3
        output = 'three';
    elseif num == 4
        output = 'four';
    elseif num == 5
        output = 'five';
    elseif num == 6
        output = 'six';
    elseif num == 7
        output = 'seven';
    elseif num == 8
        output = 'eight';
    elseif num == 9
        output = 'nine';
    elseif num == 10
        output = 'ten';
    elseif num == 11
        output ='eleven';
    elseif num == 12
        output = 'twelve';
    elseif num == 13
        output = 'thirteen';
    elseif num ==14
        output = 'fourteen';
    elseif num ==15
        output = 'fifteen';
    elseif num == 16
        output = 'sixteen';
    elseif num == 17
        output = 'seventeen';
    elseif num == 18
        output = 'eightteen';
    elseif num == 19
        output = 'nineteen';
    end
% end say_ones

